/*
    String Switch
 */

class StringSwitch {
    public static void main(String[] args) {
        String s = "627";

        switch (s) {
            case "627":
                System.out.println("627 - The Procrastination Master");
                break;
            case "rough":
                System.out.println("rough - The ROUGH One!");
                break;
            case "reborn++":
                System.out.println("reborn++ - The Coder One");
                break;
            default:
                System.out.println("rafid - The Simple Human Being");
        }
    }
}